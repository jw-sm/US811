def normalize(name: str) -> str:
    import re

    if not name:
        return ""

    name = re.sub(r"[^\w\s]", "", name.strip())
    words = name.split()
    normalized_words = []

    for word in words:
        normalized_word = STREET_ABBREVIATIONS.get(word.title(), word)
        normalized_words.append(normalized_word)

    return " ".join(normalized_words)
